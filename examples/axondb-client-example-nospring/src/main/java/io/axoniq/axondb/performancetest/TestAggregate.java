public class TestAggregate {

    private String id;
    private String data;

    public TestAggregate(String id, String data) {
        this.id = id;
        this.data = data;
    }

    public TestAggregate() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
